function getPersonalRecommendedBooks() {
  const baseBooks = filterBooksBySelectedFilters(books);
  const profileScores = buildProfileScores();
  const hasProfile = Object.keys(profileScores).length > 0;

  const scoredBooks = baseBooks.map(function (book) {
    return {
      book: book,
      score: calculateRecommendationScore(book, profileScores, hasProfile)
    };
  });

  const positiveBooks = scoredBooks
    .filter(function (item) {
      return item.score > 0;
    })
    .sort(function (a, b) {
      return b.score - a.score;
    })
    .map(function (item) {
      const copiedBook = { ...item.book };
      copiedBook.recommendationScore = item.score;
      return copiedBook;
    });

  if (positiveBooks.length > 0) {
    return positiveBooks;
  }

  return baseBooks;
}

function buildProfileScores() {
  const scores = {};

  getBooksFromIds(readBookIds).forEach(function (book) {
    addLabelsToScores(scores, getBookLabels(book), 3);
  });

  getBooksFromIds(wantBookIds).forEach(function (book) {
    addLabelsToScores(scores, getBookLabels(book), 2);
  });

  return scores;
}

function addLabelsToScores(scores, labels, point) {
  labels.forEach(function (label) {
    scores[label] = (scores[label] || 0) + point;
  });
}

function calculateRecommendationScore(book, profileScores, hasProfile) {
  const labels = getBookLabels(book);
  let score = 0;

  if (hasProfile) {
    labels.forEach(function (label) {
      score += profileScores[label] || 0;
    });
  } else {
    ["チャッピーおすすめ", "人気作", "超人気作", "読みやすい", "入門向け", "親しまれている"].forEach(function (label) {
      if (labels.includes(label)) {
        score += 2;
      }
    });
  }

  if (labels.includes("チャッピーおすすめ")) score += 3;
  if (labels.includes("人気作")) score += 2;
  if (labels.includes("超人気作")) score += 3;
  if (labels.includes("読みやすい")) score += 1;
  if (wantBookIds.includes(book.id)) score += 4;
  if (readBookIds.includes(book.id)) score -= 50;

  return score;
}

